    private void jPanel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel4MouseClicked
                try{
            String pesquisa = "select * from tbcliente where cpf like '" + txtUsuario.getText() + "' && senha = " + txtSenha.getText() + "";
            con_cliente.executaSQL(pesquisa);
            
            if (con_cliente.resultset.first()){
                homervm mostra = new homervm();
                mostra.setVisible(true);
                dispose(); 
            }
            else {
                JOptionPane.showMessageDialog(null,  "UsuÃ¡rio nÃ£o cadastrado!!!","Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
                con_cliente.desconecta();
                System.exit(0);
            }
        }
        catch(SQLException errosql){
                JOptionPane.showMessageDialog(null, "\n Os dados digitados nÃ£o foram localizados!! : \n " +errosql, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jPanel4MouseClicked

